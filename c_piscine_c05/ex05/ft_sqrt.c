int		ft_sqrt(int nb)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (j != nb)
	{
		i++;
		j = i * i;
		if (i == nb || j > nb)
			return (0);
	}
	return (i);
}
